public class ex2 {
    public static void main(String[] args) {
        // data types 
        byte a=127;  //-128 to 127
        short b=32000; //-32k to 32k
        int c=2000000000; //-2b to 2b
        long d=20000000000l; // more than 2 billion (we should add 'l'or 'L' after the number to mention it is long)
        float e=2f; // to save decimals (need to add a 'f' or 'F' to mention it is float or the java will under stand it as double)
        double f=5; // to save decimal number 
        boolean g=true; // true (or) false
        char h='a'; // holds a character which should be enter within single codes
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);
        System.out.println(f);
        System.out.println(g);
        System.out.println(h);
             

       
    }
}
