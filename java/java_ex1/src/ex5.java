public class ex5 {
        /*conditinal statement 
    1_) if 
    2_) else if
    3_) nested if 
    4_) switch case 
    */
    public static void main(String[] args) {
        int a=10,b=11;
        if (a<b){ // if statement
            System.out.println("b is greater");
        }
        else if (a>b)//else if statement
        {
            System.out.println("a is greater");
        }
        else{
            System.out.println("both are equal");
        }
    }
}
